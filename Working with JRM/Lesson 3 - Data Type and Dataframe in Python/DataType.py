from datetime import datetime

# Example code to demonstrate different data types in Python and checking their types

# Numbers
num_int = 10
num_float = 10.5
num_complex = 3 + 4j

# String
str_example = "Hello Python"

# List
list_example = [1, "two", 3.0]

# Tuple
tuple_example = (1, "two", 3.0)

# Dictionary
dict_example = {"name": "Alice", "age": 30}

# Set
set_example = {1, 2, 3}

# Boolean
bool_example = True

# Datetime
date_example = datetime(2023,12,31)

# Checking the data types

print("Integer", type(num_int))
print("Float", type(num_float))
print("Complex", type(num_complex))
print("String", type(str_example))
print("List", type(list_example))
print("Tuple", type(tuple_example))
print("Dictionary", type(dict_example))
print("Set", type(set_example))
print("Boolean", type(bool_example))
print("Boolean", type(date_example))



